package com.loiane.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("valor 1 igual valor 2: " +(valor1 == valor2));
		System.out.println("valor 1 diferente valor 2: " +(valor1 != valor2));
		System.out.println("valor 1 maior valor 2: " +(valor1 > valor2));
		System.out.println("valor 1 maior igual valor 2: " +(valor1 >= valor2));
		System.out.println("valor 1 menor valor 2: " +(valor1 < valor2));
		System.out.println("valor 1 menor igual valor 2: " +(valor1 <= valor2));

		
	}

}
